#pragma once

#include <cstdint>

namespace babrat {

    /**
      * @brief Type used as a replacement for size_t
      */
    typedef std::uint64_t index_t;

    /**
      * @brief Type used to identify connections
      */
    typedef index_t connection_t;


    /**
     * @brief Type used to count type
     */
    typedef index_t count_t;


} // namespace babrat